#!/usr/bin/python3
"""
This is a module for a class Rectnagle
"""
class Rectangle:
    """Class of a rectangle"""
    def __init__(self, width=0, height=0):
        """Initialize class"""
        self.width = width
        self.height = height

        @property
        def width(self):
            """Get width"""
            return self.__width

        @width.setter
        def width(self, widthValue):
            """Set width"""
            if type(widthValue) != int:
                raise TypeError("width must be an integer")
            if widthValue < 0:
                raise ValueError("width must be >= 0")
            self.__width = widthValue

            @property
            def height(self):
                """Get height"""
                return self.__height

        @height.setter
        def height(self, HeightValue):
            """Set height"""
            if type(HeightValue) != int:
                raise TypeError("height must be an integer")
            if HeightValue < 0:
                raise ValueError("height musr be >= 0")
            self.__height = HeightValue
